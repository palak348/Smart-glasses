    img = cv2.imread(img_name)
    r_hd   = model(img, conf=0.4)[0]
    r_qvga = model(cv2.resize(img, (320, 240)), conf=0.4)[0]

    print(f"\n=== {img_name} ===")
    print("HIGH-RES :", [model.names[int(b.cls)] for b in r_hd.boxes])
    print("QVGA     :", [model.names[int(b.cls)] for b in r_qvga.boxes])

    fig, ax = plt.subplots(1, 2, figsize=(14, 6))
    ax[0].imshow(r_hd.plot()[..., ::-1]);   ax[0].set_title(f"{img_name} - High-res"); ax[0].axis("off")
    ax[1].imshow(r_qvga.plot()[..., ::-1]); ax[1].set_title("QVGA (glasses)");          ax[1].axis("off")
    plt.show()

