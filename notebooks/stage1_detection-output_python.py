"""### 2. Load the model and run detection

`yolo26n.pt` auto-downloads the first time. `conf=0.4` keeps only detections the model is at least 40% confident about.
"""

from ultralytics import YOLO

# Stock YOLO26 Nano — pre-trained on COCO (80 common classes).
model = YOLO("yolo26n.pt")

# Run on a demo image that ships with Ultralytics (a bus + people).
results = model("https://ultralytics.com/images/bus.jpg", conf=0.4)
result = results[0]

# Stage-1 output = a simple text log of what was detected.
for box in result.boxes:
    label = model.names[int(box.cls)]
    print(f"Detected: {label}  ({float(box.conf):.2f})")

"""### 3. Show the image with boxes drawn"""

import matplotlib.pyplot as plt

# result.plot() returns the image with boxes drawn (in BGR); flip to RGB to show.
annotated = result.plot()
plt.figure(figsize=(8, 8))
plt.imshow(annotated[..., ::-1])
plt.axis("off")
plt.show()

